package javaBook.q05;

public class Exam02 {
  public static void main(String[] args) {

    int num = 120;

    if(num < 0) {
      System.out.println(num);
    }else if(0 <= num && num < 100) {
      System.out.println(num);
    }else if(100 <= num && num < 200) {
      System.out.println(num);
    }else if(200 <= num && num < 300) {
      System.out.println(num);
    }else if(300 < num) {
      System.out.println(num);
    }

  }

}
